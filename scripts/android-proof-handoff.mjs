import path from "node:path";
import {
  apkMetadata,
  listAvds,
  listDevices,
  readPackageVersion,
  repoRoot,
  resolveAndroidTools,
  resolveOutputPath,
  writeTextFile,
} from "./android-tools.mjs";

const argv = process.argv.slice(2);
const outputPath = resolveOutputPath(argv, path.join("artifacts", "android-proof-handoff.md"));
const tools = resolveAndroidTools();
const avds = listAvds(tools);
const devices = listDevices(tools);
const apk = apkMetadata(path.join(repoRoot, "GoatCitadel.apk"))
  ?? apkMetadata(path.join(repoRoot, "android", "app", "build", "outputs", "apk", "release", "app-release.apk"));

const blockers = [];
if (!tools.sdkRoot) {
  blockers.push("Android SDK root is not configured on this machine.");
}
if (!tools.adbPath) {
  blockers.push("adb is unavailable, so APK installation and device inspection remain manual.");
}
if (!tools.emulatorPath) {
  blockers.push("Android emulator binary is unavailable, so local emulator launch remains blocked.");
}
if (!apk) {
  blockers.push("No APK artifact was found at the standard repo paths.");
}

const lines = [
  "# Android Proof Handoff",
  "",
  `Generated: ${new Date().toISOString()}`,
  `Mobile version: ${readPackageVersion()}`,
  "",
  "## Summary",
  "",
  blockers.length === 0
    ? "The local machine has the minimum Android tooling required to continue emulator proofing."
    : "The local machine is not yet fully ready for end-to-end Android proofing. Current blockers are listed below.",
  "",
  "## Current blockers",
  "",
  ...(blockers.length > 0 ? blockers.map((blocker) => `- ${blocker}`) : ["- None detected by the scripted preflight."]),
  "",
  "## Tooling snapshot",
  "",
  `- SDK root: ${tools.sdkRoot ?? "missing"}${tools.sdkSource ? ` (${tools.sdkSource})` : ""}`,
  `- adb: ${tools.adbPath ?? "missing"}`,
  `- emulator: ${tools.emulatorPath ?? "missing"}`,
  "",
  "## Available AVDs",
  "",
  ...(avds.available && avds.items.length > 0 ? avds.items.map((avd) => `- ${avd}`) : [`- ${avds.error ?? "none found"}`]),
  "",
  "## Connected devices",
  "",
  ...(devices.available && devices.items.length > 0 ? devices.items.map((device) => `- ${device}`) : [`- ${devices.error ?? "none connected"}`]),
  "",
  "## APK artifact",
  "",
  ...(apk ? [
    `- Path: ${apk.path}`,
    `- Size: ${apk.size}`,
    `- SHA-256: ${apk.sha256}`,
    `- Updated: ${apk.updatedAt}`,
  ] : ["- No APK artifact detected."]),
  "",
  "## Recommended operator sequence",
  "",
  "1. Run `npm run android:doctor` and resolve any missing SDK, adb, or emulator paths.",
  "2. Run `npm run android:avd:list` and choose the AVD you want to use.",
  "3. Launch the emulator with `set ANDROID_AVD=<name> && npm run android:emulator:start`.",
  "4. Wait for the emulator to appear in `adb devices -l`.",
  "5. If the gateway is running locally on the same workstation, bridge it to the emulator with `adb reverse tcp:8787 tcp:8787`.",
  "6. Install the current APK with `npm run android:install:apk`.",
  "7. Open the login gate manually or use an adb deep link such as `adb shell am start -a android.intent.action.VIEW -d \"goatcitadel://login?url=http%3A%2F%2F127.0.0.1%3A8787\\&autoverify=1\" com.goatcitadel.mobile`.",
  "   Escape every `&` as `\\&` when launching through `adb shell`, otherwise Android treats later query params as shell control operators and silently drops them.",
  "8. Capture screenshots of the install flow, first launch, gateway sign-in, and a live command/deck screen.",
  "9. Append the screenshot paths and any manual observations to this handoff file before sharing it.",
  "",
  "## Manual proof checklist",
  "",
  "- [ ] Emulator booted successfully",
  "- [ ] APK installed successfully",
  "- [ ] App launches without immediate crash",
  "- [ ] Gateway URL and auth token entry tested",
  "- [ ] Live chat or dashboard surface verified",
  "- [ ] Screenshots captured and archived with this handoff",
  "",
  "## Notes",
  "",
  "- Generated by `npm run android:proof:handoff`.",
  "- Edit this file after manual testing to attach screenshot paths, adb logs, and any device-specific findings.",
  "",
];

writeTextFile(outputPath, `${lines.join("\n")}\n`);
console.log(`Wrote Android proof handoff to ${outputPath}`);
